    private void jButton4ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton4ActionPerformed
        // TODO add your handling code here:
   int selectedRow = jTable1.getSelectedRow();
    
        if (selectedRow == -1) {
            JOptionPane.showMessageDialog(this, "Pilih data yang ingin dihapus!");
            return;
        }
        
        String idPustakawan = jTable1.getValueAt(selectedRow, 0).toString();
        String nama = jTable1.getValueAt(selectedRow, 1).toString();
        
        int confirm = JOptionPane.showConfirmDialog(this, 
            "Apakah Anda yakin ingin menghapus data pustakawan:\n" + nama + "?", 
            "Konfirmasi Hapus", 
            JOptionPane.YES_NO_OPTION);
            
        if (confirm == JOptionPane.YES_OPTION) {
            try {
                Connection conn = DatabaseConnection.connect();
                String sql = "DELETE FROM pustakawan WHERE id_pustakawan = ?";
                PreparedStatement stmt = conn.prepareStatement(sql);
                stmt.setString(1, idPustakawan);
                
                int result = stmt.executeUpdate();
                
                if (result > 0) {
                    JOptionPane.showMessageDialog(this, "Data pustakawan berhasil dihapus!");
                    tampilkanDataPustakawan();
                } else {
                    JOptionPane.showMessageDialog(this, "Gagal menghapus data!");
                }
                
                conn.close();
                
            } catch (SQLException e) {
                JOptionPane.showMessageDialog(this, "Error saat menghapus data: " + e.getMessage());
            }
        }

    }//GEN-LAST:event_jButton4ActionPerformed

