    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        
    // Validasi apakah ada data yang dipilih
    int selectedRow = jTable1.getSelectedRow();
    
    if (selectedRow == -1) {
        JOptionPane.showMessageDialog(this, "Pilih baris yang ingin diubah.");
        return;
    }
    
    // Ambil data dari form
    String idPustakawan = jTable1.getValueAt(selectedRow, 0).toString();
    String nama = jTextField2.getText().trim();
    String email = jTextField3.getText().trim();
    String noTelepon = jTextField4.getText().trim();
    String alamat = jTextField5.getText().trim();
    String tanggalBergabung = jTextField6.getText().trim();
    String divisi = (String) jComboBox1.getSelectedItem();
    String status = (String) jComboBox2.getSelectedItem();
    
    // Validasi input
    if (nama.isEmpty() || email.isEmpty() || noTelepon.isEmpty() ||
        alamat.isEmpty() || tanggalBergabung.isEmpty() ||
        divisi == null || status == null) {
        JOptionPane.showMessageDialog(this, "Semua field wajib diisi!");
        return;
    }
    
    // Validasi format email
    if (!email.contains("@") || !email.contains(".")) {
        JOptionPane.showMessageDialog(this, "Format email tidak valid! Contoh: jumy@gmail.com");
        return;
    }
    
    // Validasi status
    if (!"Aktif".equals(status) && !"Non Aktif".equals(status)) {
        JOptionPane.showMessageDialog(this, "Status harus 'Aktif' atau 'Non Aktif'");
        return;
    }

    // Konversi status ke integer
    int statusValue = "Aktif".equals(status) ? 1 : 0;
    
    try (Connection conn = DatabaseConnection.connect()) {
        // Cek email sudah digunakan oleh pustakawan lain
        String sqlCheck = "SELECT COUNT(*) FROM pustakawan WHERE email = ? AND id_pustakawan <> ?";
        PreparedStatement stmtCheck = conn.prepareStatement(sqlCheck);
        stmtCheck.setString(1, email);
        stmtCheck.setString(2, idPustakawan);
        ResultSet rs = stmtCheck.executeQuery();
        
        if (rs.next() && rs.getInt(1) > 0) {
            JOptionPane.showMessageDialog(this, "Email sudah digunakan oleh pustakawan lain.");
            return;
        }
        
        // Update data
        String sql = "UPDATE pustakawan SET nama_pustakawan = ?, email = ?, no_telp = ?, "
                    + "alamat = ?, tanggal_bergabung = ?, divisi = ?, status = ? "
                    + "WHERE id_pustakawan = ?";
        PreparedStatement stmt = conn.prepareStatement(sql);
        
        stmt.setString(1, nama);
        stmt.setString(2, email);
        stmt.setString(3, noTelepon);
        stmt.setString(4, alamat);
        stmt.setString(5, tanggalBergabung);
        stmt.setString(6, divisi);
        stmt.setInt(7, statusValue); // Menggunakan nilai integer
        stmt.setString(8, idPustakawan);
        
        int rowsAffected = stmt.executeUpdate();
        if (rowsAffected > 0) {
            JOptionPane.showMessageDialog(this, "Data pustakawan berhasil diubah.");
            tampilkanDataPustakawan();
        } else {
            JOptionPane.showMessageDialog(this, "Tidak ada data yang diubah.");
        }
        
    } catch (SQLException e) {
        JOptionPane.showMessageDialog(this, "Error: " + e.getMessage());
        e.printStackTrace();
    }


    }//GEN-LAST:event_jButton1ActionPerformed

    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        // TODO add your handling code here:
  String nama = jTextField2.getText().trim();
    String email = jTextField3.getText().trim();
    String noTelepon = jTextField4.getText().trim();
    String alamat = jTextField5.getText().trim();
    String tanggalBergabung = jTextField6.getText().trim();
    String divisi = (String) jComboBox1.getSelectedItem();
    String status = (String) jComboBox2.getSelectedItem();

    // Validasi input
    if (nama.isEmpty() || email.isEmpty() || noTelepon.isEmpty() || 
        alamat.isEmpty() || tanggalBergabung.isEmpty()) {
        JOptionPane.showMessageDialog(this, "Semua field wajib diisi!");
        return;
    }
    
    // Validasi format email
    if (!email.contains("@") || !email.contains(".")) {
        JOptionPane.showMessageDialog(this, "Format email tidak valid! Contoh: jumy@gmail.com");
        return;
    }
    
    // Validasi divisi
    if (divisi == null || divisi.isEmpty()) {
        JOptionPane.showMessageDialog(this, "Silahkan pilih divisi terlebih dahulu.");
        return;
    }
    
    // Validasi status
    if (status == null) {
        JOptionPane.showMessageDialog(this, "Status harus dipilih!");
        return;
    }

    if (!"Aktif".equals(status) && !"Non Aktif".equals(status)) {
        JOptionPane.showMessageDialog(this, "Status harus 'Aktif' atau 'Non Aktif'");
        return;
    }

    // Konversi status ke integer
    int statusValue = "Aktif".equals(status) ? 1 : 0;

    try (Connection conn = DatabaseConnection.connect()) {
        // Cek email sudah ada atau belum
        PreparedStatement cekStmt = conn.prepareStatement("SELECT COUNT(*) FROM pustakawan WHERE email = ?");
        cekStmt.setString(1, email);
        ResultSet rs = cekStmt.executeQuery();
        rs.next();
        
        if (rs.getInt(1) > 0) {
            JOptionPane.showMessageDialog(this, "Email sudah digunakan!");
            return;
        }
        
        // Generate ID Pustakawan
        PreparedStatement idStmt = conn.prepareStatement("SELECT MAX(id_pustakawan) FROM pustakawan");
        rs = idStmt.executeQuery();
        
        int nextId = 1;
        if (rs.next()) {
            String lastId = rs.getString(1);
            if (lastId != null) {
                try {
                    int numPart = Integer.parseInt(lastId);
                    nextId = numPart + 1;
                } catch (NumberFormatException e) {
                    JOptionPane.showMessageDialog(this, "Format ID tidak valid");
                    return;
                }
            }
        }
        
        String idPustakawan = String.format("%03d", nextId);

        // Insert data baru
        String insertQuery = "INSERT INTO pustakawan (id_pustakawan, nama_pustakawan, email, no_telp, " 
                           + "alamat, tanggal_bergabung, divisi, status) VALUES (?, ?, ?, ?, ?, ?, ?, ?)";
        PreparedStatement stmt = conn.prepareStatement(insertQuery);
        stmt.setString(1, idPustakawan);
        stmt.setString(2, nama);
        stmt.setString(3, email);
        stmt.setString(4, noTelepon);
        stmt.setString(5, alamat);
        stmt.setString(6, tanggalBergabung);
        stmt.setString(7, divisi);
        stmt.setInt(8, statusValue); // Menggunakan nilai integer
        
        int rowsAffected = stmt.executeUpdate();
        
        if (rowsAffected > 0) {
            JOptionPane.showMessageDialog(this, "Data berhasil disimpan. ID Pustakawan: " + idPustakawan);
            
            // Clear form
            jTextField1.setText("");
            jTextField2.setText("");
            jTextField3.setText("");
            jTextField4.setText("");
            jTextField5.setText("");
            jTextField6.setText("");
            jComboBox1.setSelectedIndex(-1);
            jComboBox2.setSelectedIndex(-1);
            
            // Refresh tabel
            tampilkanDataPustakawan();
        } else {
            JOptionPane.showMessageDialog(this, "Gagal menyimpan data.");
        }
        
    } catch (Exception e) {
        JOptionPane.showMessageDialog(this, "Error: " + e.getMessage());
        e.printStackTrace();
    }
    }//GEN-LAST:event_jButton2ActionPerformed

