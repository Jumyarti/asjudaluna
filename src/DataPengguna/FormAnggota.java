   private void tampilkanDataAnggota() {
    try {
        Connection conn = DatabaseConnection.connect(); // atau sesuaikan dengan package kamu
        String sql = "SELECT * FROM anggota ORDER BY id_anggota";
        PreparedStatement stmt = conn.prepareStatement(sql);
        ResultSet rs = stmt.executeQuery();

        DefaultTableModel model = new DefaultTableModel();
        model.addColumn("ID Anggota");
        model.addColumn("Nama");
        model.addColumn("Alamat");
        model.addColumn("No Telepon");
        model.addColumn("Email");

        while (rs.next()) {
            model.addRow(new Object[]{
                rs.getString("id_anggota"),
                rs.getString("nama_anggota"),
                rs.getString("alamat"),
                rs.getString("telepon"),
                rs.getString("email")
            });
        }

        jTable1.setModel(model); // ganti dengan nama JTable kamu kalau beda
        conn.close();

    } catch (Exception e) {
        JOptionPane.showMessageDialog(this, "Error: " + e.getMessage());
    }

}

