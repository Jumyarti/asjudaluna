    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        // TODO add your handling code here:                                         
    int selectedRow = jTable1.getSelectedRow();

    if (selectedRow == -1) {
        JOptionPane.showMessageDialog(this, "Pilih baris yang ingin diubah.");
        return;
    }

    String id_anggota = jTextField1.getText().trim();
    String nama_anggota = jTextField2.getText().trim();
    String alamat = jTextField3.getText().trim();
    String telepon = jTextField4.getText().trim();
    String email = jTextField5.getText().trim();

    // Validasi input
    if (nama_anggota.isEmpty() || alamat.isEmpty() || telepon.isEmpty() || email.isEmpty()) {
        JOptionPane.showMessageDialog(this, "Semua field wajib diisi!");
        return;
    }

    try {
        Connection conn = DatabaseConnection.connect();

        // Cek apakah email sudah dipakai oleh anggota lain
        String sqlCheck = "SELECT COUNT(*) FROM anggota WHERE email = ? AND id_anggota <> ?";
        PreparedStatement stmtCheck = conn.prepareStatement(sqlCheck);
         stmtCheck.setString(1, email);
        stmtCheck.setString(2, id_anggota);
        ResultSet rs = stmtCheck.executeQuery();

        if (rs.next() && rs.getInt(1) > 0) {
            JOptionPane.showMessageDialog(this, "Email sudah digunakan oleh anggota lain.");
            return;
        }

        // Update data anggota
        String sql = "UPDATE anggota SET nama_anggota = ?, alamat = ?, telepon = ?, email = ? WHERE id_anggota = ?";
        PreparedStatement stmt = conn.prepareStatement(sql);
        stmt.setString(1, nama_anggota);
        stmt.setString(2, alamat);
        stmt.setString(3, telepon);
        stmt.setString(4, email);
        stmt.setString(5, id_anggota);

        stmt.executeUpdate();
        JOptionPane.showMessageDialog(this, "Data anggota berhasil diubah.");

        tampilkanDataAnggota(); // refresh tabel
        conn.close();

    } catch (SQLException e) {
        JOptionPane.showMessageDialog(this, "Gagal menyimpan: " + e.getMessage());
    }
    }//GEN-LAST:event_jButton2ActionPerformed

    private void jButton4ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton4ActionPerformed
        // TODO add your handling code here:                                        
    int selectedRow = jTable1.getSelectedRow();

    if (selectedRow == -1) {
        JOptionPane.showMessageDialog(this, "Pilih data yang ingin dihapus!");
        return;
    }

    String idAnggota = jTable1.getValueAt(selectedRow, 0).toString();
    String nama = jTable1.getValueAt(selectedRow, 1).toString();

    // Konfirmasi penghapusan
    int confirm = JOptionPane.showConfirmDialog(this, 
        "Apakah Anda yakin ingin menghapus data anggota:\n" + nama + "?",
        "Konfirmasi Hapus",
        JOptionPane.YES_NO_OPTION);

    if (confirm == JOptionPane.YES_OPTION) {
        try {
            Connection conn = DatabaseConnection.connect();
            String sql = "DELETE FROM anggota WHERE id_anggota = ?";
            PreparedStatement stmt = conn.prepareStatement(sql);
            stmt.setString(1, idAnggota);

            int result = stmt.executeUpdate();

            if (result > 0) {
                JOptionPane.showMessageDialog(this, "Data anggota berhasil dihapus!");
                tampilkanDataAnggota(); // Refresh tabel
            } else {
                JOptionPane.showMessageDialog(this, "Gagal menghapus data!");
            }

            conn.close();

        } catch (SQLException e) {
            JOptionPane.showMessageDialog(this, "Error saat menghapus data: " + e.getMessage());
        }
    }

    }//GEN-LAST:event_jButton4ActionPerformed

