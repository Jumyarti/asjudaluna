    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        // TODO add your handling code here:                                      
   String nama = jTextField2.getText().trim();
String alamat = jTextField3.getText().trim();
String telepon = jTextField4.getText().trim();
String email = jTextField5.getText().trim();

if (nama.isEmpty() || alamat.isEmpty() || telepon.isEmpty() || email.isEmpty()) {
    JOptionPane.showMessageDialog(this, "Semua field wajib diisi!");
    return;
}

try {
    Connection conn = DatabaseConnection.connect();

    // Ambil ID terakhir dari database
    String getLastIdSql = "SELECT MAX(id_anggota) FROM anggota";
    PreparedStatement getLastIdStmt = conn.prepareStatement(getLastIdSql);
    ResultSet rs = getLastIdStmt.executeQuery();

    int nextId = 1;
    if (rs.next()) {
        nextId = rs.getInt(1) + 1;
    }

    // Simpan data termasuk ID
    String sql = "INSERT INTO anggota (id_anggota, nama_anggota, alamat, telepon, email) VALUES (?, ?, ?, ?, ?)";
    PreparedStatement stmt = conn.prepareStatement(sql);
    stmt.setInt(1, nextId);
    stmt.setString(2, nama);
    stmt.setString(3, alamat);
    stmt.setString(4, telepon);
    stmt.setString(5, email);

    stmt.executeUpdate();
    JOptionPane.showMessageDialog(this, "Data anggota berhasil disimpan!");

    tampilkanDataAnggota(); // refresh tabel
    conn.close();

} catch (SQLException e) {
    JOptionPane.showMessageDialog(this, "Gagal menyimpan data: " + e.getMessage());
}
    }//GEN-LAST:event_jButton3ActionPerformed

