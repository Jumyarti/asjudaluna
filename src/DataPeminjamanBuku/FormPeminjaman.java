    private void jButton4ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton4ActionPerformed
        // TODO add your handling code here:                                       
    int selectedRow = jTable1.getSelectedRow();

    if (selectedRow == -1) {
        JOptionPane.showMessageDialog(this, "Pilih data yang ingin dihapus!");
        return;
    }

    String idPeminjaman = jTable1.getValueAt(selectedRow, 0).toString();
    String idAnggota = jTable1.getValueAt(selectedRow, 1).toString();

    // Konfirmasi penghapusan
    int confirm = JOptionPane.showConfirmDialog(this, 
        "Apakah Anda yakin ingin menghapus data peminjaman milik:\n" + idAnggota + "?",
        "Konfirmasi Hapus",
        JOptionPane.YES_NO_OPTION);

    if (confirm == JOptionPane.YES_OPTION) {
        try {
            Connection conn = DatabaseConnection.connect();
            String sql = "DELETE FROM peminjaman WHERE id_peminjaman = ?";
            PreparedStatement stmt = conn.prepareStatement(sql);
            stmt.setString(1, idPeminjaman);

            int result = stmt.executeUpdate();

            if (result > 0) {
                JOptionPane.showMessageDialog(this, "Data peminjaman berhasil dihapus!");
                tampilkanDataPeminjaman(); // Refresh tabel, pastikan kamu punya method ini
            } else {
                JOptionPane.showMessageDialog(this, "Gagal menghapus data!");
            }

            conn.close();

        } catch (SQLException e) {
            JOptionPane.showMessageDialog(this, "Error saat menghapus data: " + e.getMessage());
        }
    }
    }//GEN-LAST:event_jButton4ActionPerformed

