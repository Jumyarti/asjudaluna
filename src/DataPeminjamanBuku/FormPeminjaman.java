    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:                                                                     
    String idPeminjaman = jTextField1.getText().trim();
    String idAnggota = jTextField2.getText().trim();
    String idBuku = jTextField3.getText().trim();
    String tanggalPinjam = jTextField4.getText().trim();
    String tanggalKembaliHarus = jTextField5.getText().trim();
    String tanggalKembaliAktual = jTextField6.getText().trim();
    String statusPinjam = jTextField7.getText().trim();
    String denda = jTextField8.getText().trim();

    if (idAnggota.isEmpty() || idBuku.isEmpty() || tanggalPinjam.isEmpty() || tanggalKembaliHarus.isEmpty() || statusPinjam.isEmpty()) {
        JOptionPane.showMessageDialog(this, "Semua field wajib diisi kecuali ID dan Tanggal Kembali Aktual!");
        return;
    }

    try {
        Connection conn = DatabaseConnection.connect();

        // Cek ID terakhir untuk peminjaman (auto-generate ID jika kosong)
        if (idPeminjaman.isEmpty()) {
            String getLastIdSql = "SELECT MAX(id_peminjaman) FROM peminjaman";
            PreparedStatement getLastIdStmt = conn.prepareStatement(getLastIdSql);
            ResultSet rs = getLastIdStmt.executeQuery();

            int nextId = 1;
            if (rs.next() && rs.getString(1) != null) {
                String lastId = rs.getString(1);
                try {
                    int numPart = Integer.parseInt(lastId.substring(2));
                    nextId = numPart + 1;
                } catch (NumberFormatException e) {
                    nextId = 1;
                }
            }
            idPeminjaman = String.format("PJ%03d", nextId);
        }

        String sql = "INSERT INTO peminjaman (id_peminjaman, id_anggota, id_buku, tanggal_pinjam, tanggal_kembali_Harus, tanggal_kembali_aktual, status_pinjam, denda) VALUES (?, ?, ?, ?, ?, ?, ?, ?)";
        PreparedStatement stmt = conn.prepareStatement(sql);
        stmt.setString(1, idPeminjaman);
        stmt.setString(2, idAnggota);
        stmt.setString(3, idBuku);
        stmt.setString(4, tanggalPinjam);
        stmt.setString(5, tanggalKembaliHarus);
        stmt.setString(6, tanggalKembaliAktual.isEmpty() ? null : tanggalKembaliAktual);
        stmt.setString(7, statusPinjam);
        stmt.setString(8, denda.isEmpty() ? "0" : denda);

        stmt.executeUpdate();
        JOptionPane.showMessageDialog(this, "Data peminjaman berhasil ditambahkan!");
        
        tampilkanDataPeminjaman(); // refresh table
        conn.close();

        // Reset field setelah tambah
        jTextField1.setText("");
        jTextField2.setText("");
        jTextField3.setText("");
        jTextField4.setText("");
        jTextField5.setText("");
        jTextField6.setText("");
        jTextField7.setText("");
        jTextField8.setText("");
        jTextField2.requestFocus();

    } catch (SQLException e) {
        JOptionPane.showMessageDialog(this, "Gagal menyimpan data: " + e.getMessage());
    }

    }//GEN-LAST:event_jButton1ActionPerformed

