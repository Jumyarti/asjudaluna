    private void tampilkanDataPeminjaman() {
        try {
            Connection conn = DatabaseConnection.connect();
            String sql = "SELECT * FROM peminjaman ORDER BY id_peminjaman";
            PreparedStatement stmt = conn.prepareStatement(sql);
            ResultSet rs = stmt.executeQuery();
              DefaultTableModel model = new DefaultTableModel();
            model.addColumn("ID Peminjaman");
            model.addColumn("ID Anggota");
            model.addColumn("ID Buku");
            model.addColumn("Tanggal Pinjam");
            model.addColumn("Tanggal Kembali harus");
            model.addColumn("Tanggal Kembali Aktual");
            model.addColumn("Status Pinjam");
            model.addColumn("Denda");

            while (rs.next()) {
                model.addRow(new Object[]{
                    rs.getString("id_peminjaman"),
                    rs.getString("id_anggota"),
                    rs.getString("id_buku"),
                    rs.getString("tanggal_pinjam"),
                    rs.getString("tanggal_kembali_harus"),
                    rs.getString("tanggal_kembali_aktual"),
                    rs.getString("status_pinjam"),
                    rs.getString("denda")
                });
            }

            jTable1.setModel(model);
            conn.close();
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Error: " + e.getMessage());
        }
    }

