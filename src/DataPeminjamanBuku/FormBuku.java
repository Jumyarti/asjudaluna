    private void tampilkanDataBuku() {
        Connection conn = null;
        PreparedStatement stmt = null;
        ResultSet rs = null;
        try {
            conn = DatabaseConnection.connect();
            String sql = "SELECT * FROM buku ORDER BY id_buku";
            stmt = conn.prepareStatement(sql);
            rs = stmt.executeQuery();

            DefaultTableModel model = new DefaultTableModel();
            model.addColumn("ID Buku");
            model.addColumn("Judul");
            model.addColumn("Penulis");
            model.addColumn("Penerbit");
            model.addColumn("Tahun Terbit");
            model.addColumn("ISBN");
            model.addColumn("Jumlah Stok");
            model.addColumn("Kategori");

            while (rs.next()) {
                model.addRow(new Object[]{
                    rs.getString("id_buku"),
                    rs.getString("judul"),
                    rs.getString("penulis"),
                    rs.getString("penerbit"),
                    rs.getString("tahun_terbit"),
                    rs.getString("isbn"),
                    rs.getInt("jumlah_stok"),
                    rs.getString("kategori")
                });
            }

            jTable1.setModel(model);
        } catch (SQLException e) {
            JOptionPane.showMessageDialog(this, "Gagal menampilkan data: " + e.getMessage());
        } finally {
            try {
                if (rs != null) rs.close();
                if (stmt != null) stmt.close();
                if (conn != null) conn.close();
            } catch (SQLException ex) {
                JOptionPane.showMessageDialog(this, "Error saat menutup sumber daya: " + ex.getMessage());
            }
        }
    }

